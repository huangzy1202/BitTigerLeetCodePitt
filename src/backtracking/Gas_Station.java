package backtracking;

public class Solution {
    // https://leetcode.com/discuss/4159/share-some-of-my-ideas
    // https://leetcode.com/discuss/16087/space-running-time-solution-anybody-have-posted-this-solution
    
    public int canCompleteCircuit(int[] gas, int[] cost) {
        int sumGas = 0;
        int sumCost = 0;
        int start = 0;
        int tank = 0;
        for (int i = 0; i < gas.length; i++) {
            sumGas += gas[i];
            sumCost += cost[i];
            tank += gas[i] - cost[i];
            if (tank < 0) {
                start = i + 1;
                tank = 0;
            }
        }
        if (sumGas < sumCost) {
            return -1;
        } else {
            return start;
        }
    }
}